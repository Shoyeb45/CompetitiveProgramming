void solve() {
    int n;
    cin >> n;
    map<int, int> mp;
    int x = n * (n - 1) / 2;

    while (x--) {
        int t;
        cin >> t;
        mp[t]++;
    }   
    vector<int> ans;
    
    for (auto it: mp) {
        int freq = it.second;
        while (freq > 0) {
            ans.push_back(it.first);
            freq -= n - ans.size();
        }

    }

    while (ans.size() < n) {
        ans.push_back(1e9);
    }
    for (int i = 0; i < n; i++) {
        cout << ans[i] << " \n"[i == n - 1];
    }
}


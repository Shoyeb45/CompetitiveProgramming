void solve() {
    string s;
    cin >> s;

    int extra = 0;
    stack<char> st;
    for(int i = 0; i < s.size(); i++) {
        if(s[i] == '(') {
            st.push('(');
        }
        else {
            if(!st.empty() && st.top() == '(') {
                st.pop();
            } 
            else {
                extra++;
            }
        }
    }

    cout << (int)(s.size() - st.size() - extra) << "\n";
}

